import numpy as np
# Numpy es una libreria de Python que permite trabajar con arreglos multidimensionales y matrices, ademas de contar con funciones matematicas de alto nivel.


array = np.array([1, 2, 3, 4, 5])
print(array)
# El objeto array es un arreglo de una dimension, pero se pueden crear arreglos de dos o mas dimensiones.
array2 = np.array([[1, 2, 3], [4, 5, 6]])   
print(array2)
